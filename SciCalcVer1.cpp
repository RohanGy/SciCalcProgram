#include "SciCalcVer1.hpp"
#include <limits>
template<class T1,class T2>
float operator +(T1 a,T2 b){
    return a+b;
}
template<class T1,class T2>
float operator - (T1 a,T2 b){
    return a-b;
}
template<class T1,class T2>
float operator * (T1 a,T2 b){
    return a*b;
}
template<class T1,class T2>
float operator /(T1 a, T2 b){
    return a/b;
}
